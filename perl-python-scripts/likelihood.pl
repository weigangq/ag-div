#!/usr/bin/env perl
use warnings;
use strict;
use Bio::AlignIO;
use Inline::Files;
use Data::Dumper;
#use Bio::PopGen::Utilities;

my $cutoff = 0.05;
my %total;
while(<XREACT>) {
	chomp;
	next if /^est/;
	my @data = split;
#	next if $data[2]=~/^N/;
	my $est = 0;
	$est = $data[0]>0? 1 : -1 if $data[1]<$cutoff;
	$total{$data[2]}{$data[3]} = $est;
	$total{$data[3]}{$data[2]} = $est;
}
#print Dumper(\%total); exit;

my $aln = Bio::AlignIO->new(-fh=>\*ALN)->next_aln();
my %seqs;
foreach ($aln->each_seq){
    next if $_->id =~ /^N[12]/; # N17/N18/N19/N20
    my @aa = split //, $_->seq(); 
    $seqs{$_->id()} = \@aa
}
#print Dumper(\%seqs); exit;
#my $pop = Bio::PopGen::Utilities->aln_to_population(-alignment => $aln);
#my @pos = $pop->get_marker_names;
my @pos;
my $matchLine = $aln->match_line();
my @match_symbols = split //, $matchLine;
for (my $i = 0; $i < $aln->length; $i++) { 
	push @pos, $i+1 unless $match_symbols[$i] eq '*'; # || $match_symbols[$i] eq ':'
} # use only significant mismatches
#print scalar @pos, "\n"; 

my %collect;
foreach (keys %seqs){
	my @aa = @{$seqs{$_}};
	foreach my $p (@pos){
		$collect{$p}{$aa[$p-1]}++
	}
}
#print Dumper(\%collect);

my @position;
foreach my $p (keys %collect){
	my %obj = %{$collect{$p}};
	my @al = keys %obj;
	push @position, $p unless scalar @al==2 && ($al[0] eq '-' || $al[1] eq '-');
}

#print scalar @position, "\n"; exit;
print join "\t", qw(allele pos near far med);
print "\n";
foreach my $id (sort keys %total){
	my %allele = %{$total{$id}};
	#print Dumper(\%allele); exit;

	my @seq = @{$seqs{$id}};
	my %seqCmp;
	foreach my $id2 (keys %seqs){
		next if $id2 eq $id;
		my $est = $allele{$id2};
		my @seq2 = @{$seqs{$id2}};
		foreach (@position){
			$seqCmp{$_}{$est}++ if $seq[$_-1] eq $seq2[$_-1]
		}
	}
	#print Dumper(\%seqCmp); exit;

	my %sum;
	$sum{$allele{$_}}++ foreach keys %allele;
	#print Dumper(\%sum); exit;

	foreach my $p (sort {$a<=>$b} keys %seqCmp){
		my %ct = %{$seqCmp{$p}};
		my @pp;
		foreach my $cls (1,-1,0){
			my $c = $ct{$cls};
			my $p;
			if ($c){
				my $s = $sum{$cls};
#				$p = $cls==1? (1+$c+1)/(2+$s) : (1+$c)/(2+$s);
				$p = $cls==1?  (1+$c+1)/(2+$s+1) : (1+$c)/(2+$s)
			} else {
				$p = $cls==1? 2/3 : 0.5
			}
			$p = sprintf "%.4f", $p;
			push @pp, $p
		}
#		my @pp = map {$ct{$_}? sprintf "%.4f", ($_==1? ($ct{$_}+1)/($sum{$_}+1) : $ct{$_}/$sum{$_}) : ($sum{$_} && $_ !=1? 0 : 1)} (1,-1,0);
		print join "\t", ($id, $p, @pp);
		print "\n"
	}
}
exit;

